CREATE SCHEMA IF NOT EXISTS code_room DEFAULT CHARACTER SET utf8mb4 COLLATE utf8mb4_unicode_ci;

USE code_room;
CREATE TABLE IF NOT EXISTS `region` (
  `id` INT NOT NULL AUTO_INCREMENT,
  `code` VARCHAR(10) NOT NULL,
  `name` VARCHAR(100) NOT NULL,
  `romanNumber` VARCHAR(10) NOT NULL,
  `createdAt` DATETIME(3) NOT NULL DEFAULT CURRENT_TIMESTAMP(3),
  `updatedAt` DATETIME(3) NOT NULL DEFAULT CURRENT_TIMESTAMP(3) ON UPDATE CURRENT_TIMESTAMP(3),
  PRIMARY KEY (`id`),
  UNIQUE KEY `region_code_key` (`code`),
  KEY `region_code_idx` (`code`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

CREATE TABLE IF NOT EXISTS `amenity` (
  `id` INT NOT NULL AUTO_INCREMENT,
  `name` VARCHAR(100) NOT NULL,
  `description` TEXT NULL,
  `icon` VARCHAR(50) NULL,
  `category` VARCHAR(50) NULL,
  `createdAt` DATETIME(3) NOT NULL DEFAULT CURRENT_TIMESTAMP(3),
  `updatedAt` DATETIME(3) NOT NULL DEFAULT CURRENT_TIMESTAMP(3) ON UPDATE CURRENT_TIMESTAMP(3),
  PRIMARY KEY (`id`),
  UNIQUE KEY `amenity_name_key` (`name`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

CREATE TABLE IF NOT EXISTS `comuna` (
  `id` INT NOT NULL AUTO_INCREMENT,
  `name` VARCHAR(100) NOT NULL,
  `regionId` INT NOT NULL,
  `createdAt` DATETIME(3) NOT NULL DEFAULT CURRENT_TIMESTAMP(3),
  `updatedAt` DATETIME(3) NOT NULL DEFAULT CURRENT_TIMESTAMP(3) ON UPDATE CURRENT_TIMESTAMP(3),
  PRIMARY KEY (`id`),
  KEY `comuna_regionId_idx` (`regionId`),
  KEY `comuna_name_regionId_idx` (`name`, `regionId`),
  CONSTRAINT `Comuna_region_fkey` FOREIGN KEY (`regionId`) REFERENCES `region` (`id`) ON DELETE CASCADE ON UPDATE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

CREATE TABLE IF NOT EXISTS `student` (
  `id` INT NOT NULL AUTO_INCREMENT,
  `studentRut` VARCHAR(191) NOT NULL,
  `studentEmail` VARCHAR(191) NOT NULL,
  `studentName` VARCHAR(191) NOT NULL,
  `password` VARCHAR(191) NOT NULL,
  `studentCollege` VARCHAR(191) NOT NULL,
  `studentCertificateUrl` VARCHAR(191) NOT NULL,
  `role` VARCHAR(191) NOT NULL,
  `comunaId` INT NOT NULL,
  `regionId` INT NOT NULL,
  `createdAt` DATETIME(3) NOT NULL DEFAULT CURRENT_TIMESTAMP(3),
  `updatedAt` DATETIME(3) NOT NULL DEFAULT CURRENT_TIMESTAMP(3) ON UPDATE CURRENT_TIMESTAMP(3),
  PRIMARY KEY (`id`),
  UNIQUE KEY `student_studentRut_key` (`studentRut`),
  UNIQUE KEY `student_studentEmail_key` (`studentEmail`),
  KEY `student_comunaId_idx` (`comunaId`),
  KEY `student_regionId_idx` (`regionId`),
  CONSTRAINT `Student_comuna_fkey` FOREIGN KEY (`comunaId`) REFERENCES `comuna` (`id`) ON DELETE RESTRICT ON UPDATE CASCADE,
  CONSTRAINT `Student_region_fkey` FOREIGN KEY (`regionId`) REFERENCES `region` (`id`) ON DELETE RESTRICT ON UPDATE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

CREATE TABLE IF NOT EXISTS `landlord` (
  `id` INT NOT NULL AUTO_INCREMENT,
  `landlordRut` VARCHAR(191) NOT NULL,
  `landlordEmail` VARCHAR(191) NOT NULL,
  `landlordName` VARCHAR(191) NOT NULL,
  `password` VARCHAR(191) NOT NULL,
  `landlordCarnetUrl` VARCHAR(191) NOT NULL,
  `role` VARCHAR(191) NOT NULL DEFAULT 'landlord',
  `comunaId` INT NOT NULL,
  `regionId` INT NOT NULL,
  `createdAt` DATETIME(3) NOT NULL DEFAULT CURRENT_TIMESTAMP(3),
  `updatedAt` DATETIME(3) NOT NULL DEFAULT CURRENT_TIMESTAMP(3) ON UPDATE CURRENT_TIMESTAMP(3),
  PRIMARY KEY (`id`),
  UNIQUE KEY `landlord_landlordRut_key` (`landlordRut`),
  UNIQUE KEY `landlord_landlordEmail_key` (`landlordEmail`),
  KEY `landlord_comunaId_idx` (`comunaId`),
  KEY `landlord_regionId_idx` (`regionId`),
  CONSTRAINT `Landlord_comuna_fkey` FOREIGN KEY (`comunaId`) REFERENCES `comuna` (`id`) ON DELETE RESTRICT ON UPDATE CASCADE,
  CONSTRAINT `Landlord_region_fkey` FOREIGN KEY (`regionId`) REFERENCES `region` (`id`) ON DELETE RESTRICT ON UPDATE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

CREATE TABLE IF NOT EXISTS `property` (
  `id` INT NOT NULL AUTO_INCREMENT,
  `landlordId` INT NOT NULL,
  `title` VARCHAR(191) NOT NULL,
  `description` TEXT NULL,
  `address` VARCHAR(191) NOT NULL,
  `comunaId` INT NOT NULL,
  `regionId` INT NOT NULL,
  `propertyType` ENUM('APARTMENT', 'HOUSE', 'ROOM') NOT NULL,
  `bedrooms` INT NOT NULL,
  `bathrooms` INT NOT NULL,
  `squareMeters` DOUBLE NULL,
  `monthlyRent` DECIMAL(10, 2) NOT NULL,
  `isAvailable` BOOLEAN NOT NULL DEFAULT TRUE,
  `utilityBillUrl` VARCHAR(191) NOT NULL,
  `utilityBillValidated` BOOLEAN NOT NULL DEFAULT FALSE,
  `createdAt` DATETIME(3) NOT NULL DEFAULT CURRENT_TIMESTAMP(3),
  `updatedAt` DATETIME(3) NOT NULL DEFAULT CURRENT_TIMESTAMP(3) ON UPDATE CURRENT_TIMESTAMP(3),
  PRIMARY KEY (`id`),
  KEY `property_landlordId_idx` (`landlordId`),
  KEY `property_comunaId_idx` (`comunaId`),
  KEY `property_regionId_idx` (`regionId`),
  CONSTRAINT `property_landlord_fkey` FOREIGN KEY (`landlordId`) REFERENCES `landlord` (`id`) ON DELETE CASCADE ON UPDATE CASCADE,
  CONSTRAINT `Property_comuna_fkey` FOREIGN KEY (`comunaId`) REFERENCES `comuna` (`id`) ON DELETE RESTRICT ON UPDATE CASCADE,
  CONSTRAINT `Property_region_fkey` FOREIGN KEY (`regionId`) REFERENCES `region` (`id`) ON DELETE RESTRICT ON UPDATE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

CREATE TABLE IF NOT EXISTS `propertyimage` (
  `id` INT NOT NULL AUTO_INCREMENT,
