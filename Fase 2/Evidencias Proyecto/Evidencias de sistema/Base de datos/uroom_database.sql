  `updatedAt` DATETIME(3) NOT NULL DEFAULT CURRENT_TIMESTAMP(3) ON UPDATE CURRENT_TIMESTAMP(3),
  PRIMARY KEY (`id`),
  KEY `propertyimage_propertyId_displayOrder_idx` (`propertyId`, `displayOrder`),
  KEY `propertyimage_propertyId_isPrimary_idx` (`propertyId`, `isPrimary`),
  CONSTRAINT `propertyimage_property_fkey` FOREIGN KEY (`propertyId`) REFERENCES `property` (`id`) ON DELETE CASCADE ON UPDATE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

CREATE TABLE IF NOT EXISTS `propertyamenity` (
  `id` INT NOT NULL AUTO_INCREMENT,
